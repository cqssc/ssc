        /// 执行存储过程，返回影响的行数		
        /// </summary>
        /// <param name="storedProcName">存储过程名</param>
        /// <param name="parameters">存储过程参数</param>
        /// <param name="rowsAffected">影响的行数</param>
        /// <returns></returns>
        public static void RunProcedure(string storedProcName, IDataParameter[] parameters, out int rowsAffected)
        {
            using (SqlConnection connection = new SqlConnection(connectionString))
            {
                connection.Open();
                SqlCommand command = BuildQueryCommand(connection, storedProcName, parameters);
                rowsAffected = command.ExecuteNonQuery();
                connection.Close();
                connection.Dispose();
            }
        }
        public static void RunProcedureNoReturn(string storedProcName, IDataParameter[] parameters)
        {
            using (SqlConnection connection = new SqlConnection(connectionString))
            {
                int rowsAffected;
                connection.Open();
                SqlCommand command = BuildQueryCommand(connection, storedProcName, parameters);
                rowsAffected = command.ExecuteNonQuery();
                connection.Close();
                connection.Dispose();
            }
        }
        public static void RunProcedure(SqlTransaction tran, string storedProcName, IDataParameter[] parameters)
        {
            using (SqlCommand cmd = new SqlCommand())
            {
                try
                {
                    BuildQueryCommand(cmd, tran.Connection, tran, storedProcName, parameters);
                    cmd.ExecuteNonQuery();
                    cmd.Parameters.Clear();
                }
                catch (SqlException E)
                {
                    throw new Exception(E.Message);
                }
            }
        }

        /// <summary>
        /// 创建 SqlCommand 对象实例(用来返回一个整数值)	
        /// </summary>
        /// <param name="storedProcName">存储过程名</param>
        /// <param name="parameters">存储过程参数</param>
        /// <returns>SqlCommand 对象实例</returns>
        private static SqlCommand BuildIntCommand(SqlConnection connection, string storedProcName, IDataParameter[] parameters)
        {
            SqlCommand command = BuildQueryCommand(connection, storedProcName, parameters);
            command.Parameters.Add(new SqlParameter("ReturnValue",
                SqlDbType.Int, 4, ParameterDirection.ReturnValue,
                false, 0, 0, string.Empty, DataRowVersion.Default, null));
            return command;
        }
        public static void UpdateDateTable(DataTable dt)
        {
            using (SqlConnection conn = new SqlConnection(connectionString))
            {
                conn.Open();
                SqlDataAdapter adapter = new SqlDataAdapter("select * from " + dt.TableName, conn);
                SqlCommandBuilder cb = new SqlCommandBuilder(adapter);
                adapter.Update(dt);
            }
        }

        public static void UpdateDateTable(SqlTransaction trans, DataTable dt)
        {
            SqlCommand selectCommand = new SqlCommand("select * from " + dt.TableName, trans.Connection, trans);
            SqlDataAdapter adapter = new SqlDataAdapter(selectCommand);
            SqlCommandBuilder cb = new SqlCommandBuilder(adapter);
            cb.GetInsertCommand().Transaction = trans;
            cb.GetDeleteCommand().Transaction = trans;
            cb.GetUpdateCommand().Transaction = trans;
            adapter.Update(dt);
        }

